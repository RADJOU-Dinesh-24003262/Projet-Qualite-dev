package org.example.model.food;

/**
 * Enumeration of food type categories.
 * <p>
 * This enum categorizes different types of food available in the game.
 * Each food item belongs to one of these categories:
 * </p>
 * <ul>
 *     <li>MEAT - Animal meat products (boar, etc.)</li>
 *     <li>SEAFOOD - Sea-based food items</li>
 *     <li>FISH - Fish and aquatic creatures (lobster, etc.)</li>
 *     <li>VEGETABLE - Plant-based food (mistletoe, carrots, strawberries)</li>
 *     <li>DRINK - Beverages (wine, mead, beer)</li>
 *     <li>MAGIC - Magical food items with special effects</li>
 *     <li>OTHER - Miscellaneous food items</li>
 * </ul>
 */
public enum FoodType {
    MEAT,
    SEAFOOD,
    FISH,
    VEGETABLE,
    DRINK,
    MAGIC,
    OTHER,
}
